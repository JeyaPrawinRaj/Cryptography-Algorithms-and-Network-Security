# ================= BASIC FUNCTIONS =================
def xor_bytes(a, b):
    return bytes(x ^ y for x, y in zip(a, b))

def left_shift(block):
    res = bytearray(16)
    carry = 0
    for i in range(15, -1, -1):
        res[i] = ((block[i] << 1) & 0xFF) | carry
        carry = (block[i] >> 7) & 1
    return bytes(res)

def pad(block):
    return block + b'\x80' + b'\x00' * (15 - len(block))

# ================= AES HELPERS =================
def sub_bytes(state):
    return [S_BOX[b] for b in state]

def shift_rows(s):
    return [
        s[0], s[5], s[10], s[15],
        s[4], s[9], s[14], s[3],
        s[8], s[13], s[2], s[7],
        s[12], s[1], s[6], s[11]
    ]

def gmul(a, b):
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        hi = a & 0x80
        a <<= 1
        if hi:
            a ^= 0x1B
        b >>= 1
    return p & 0xFF

def mix_columns(s):
    out = [0]*16
    for c in range(4):
        col = s[c*4:(c+1)*4]
        out[c*4+0] = gmul(2,col[0]) ^ gmul(3,col[1]) ^ col[2] ^ col[3]
        out[c*4+1] = col[0] ^ gmul(2,col[1]) ^ gmul(3,col[2]) ^ col[3]
        out[c*4+2] = col[0] ^ col[1] ^ gmul(2,col[2]) ^ gmul(3,col[3])
        out[c*4+3] = gmul(3,col[0]) ^ col[1] ^ col[2] ^ gmul(2,col[3])
    return out

def add_round_key(state, key):
    return [s ^ k for s, k in zip(state, key)]

# ================= AES BLOCK ENCRYPT =================
def aes_encrypt_block(block, key):
    state = list(block)

    print("\n--- AES BLOCK ENCRYPTION ---")
    print("Input Block:", bytes(state).hex())

    state = add_round_key(state, key)
    print("After AddRoundKey:", bytes(state).hex())

    for r in range(9):
        state = sub_bytes(state)
        state = shift_rows(state)
        state = mix_columns(state)
        state = add_round_key(state, key)
        print(f"Round {r+1}:", bytes(state).hex())

    state = sub_bytes(state)
    state = shift_rows(state)
    state = add_round_key(state, key)

    print("Final State:", bytes(state).hex())
    return bytes(state)

# ================= CMAC =================
def generate_subkeys(key):
    L = aes_encrypt_block(b'\x00'*16, key)

    print("\n--- Subkey Generation ---")
    print("L =", L.hex())

    K1 = left_shift(L)
    if L[0] & 0x80:
        K1 = K1[:-1] + bytes([K1[-1] ^ 0x87])
    print("K1 =", K1.hex())

    K2 = left_shift(K1)
    if K1[0] & 0x80:
        K2 = K2[:-1] + bytes([K2[-1] ^ 0x87])
    print("K2 =", K2.hex())

    return K1, K2

def aes_cmac(message, key):
    K1, K2 = generate_subkeys(key)

    blocks = [message[i:i+16] for i in range(0, len(message), 16)]
    if len(blocks) == 0:
        blocks = [b'']

    print("\n--- Message Blocks ---")
    for i, b in enumerate(blocks):
        print(f"Block {i}:", b.hex())

    if len(blocks[-1]) == 16:
        last = xor_bytes(blocks[-1], K1)
        print("\nComplete block XOR K1")
    else:
        padded = pad(blocks[-1])
        print("\nPadded block:", padded.hex())
        last = xor_bytes(padded, K2)
        print("Final Block:", last.hex())

    X = b'\x00'*16
    print("\nInitial X:", X.hex())

    for i, block in enumerate(blocks[:-1]):
        print(f"\nProcessing Block {i}")
        X = aes_encrypt_block(xor_bytes(X, block), key)

    print("\nFinal Step:")
    T = aes_encrypt_block(xor_bytes(X, last), key)

    print("\nCMAC Tag:", T.hex())
    return T.hex()

